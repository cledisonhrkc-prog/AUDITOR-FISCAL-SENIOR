-- Adiciona FK de plano em tenants de forma idempotente
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'fk_tenants_plano'
  ) THEN
    ALTER TABLE tenants ADD CONSTRAINT fk_tenants_plano 
      FOREIGN KEY (plano_id) REFERENCES planos(id) ON DELETE SET NULL;
  END IF;
END;
$$;

