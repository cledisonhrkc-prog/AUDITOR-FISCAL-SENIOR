-- [BLOCO 3] Fator R (Cálculo Folha / Faturamento)
CREATE OR REPLACE FUNCTION fn_calcular_fator_r(
  p_folha_pagamento_12m NUMERIC,
  p_receita_bruta_12m NUMERIC
) RETURNS JSONB LANGUAGE plpgsql AS $$
DECLARE
  v_fator_r NUMERIC(8,6);
  v_anexo_recomendado TEXT;
BEGIN
  IF p_receita_bruta_12m IS NULL OR p_receita_bruta_12m <= 0 THEN
    RETURN jsonb_build_object('erro', 'Receita bruta acumulada (RBT12) inválida.');
  END IF;
  IF p_folha_pagamento_12m IS NULL OR p_folha_pagamento_12m < 0 THEN
    RETURN jsonb_build_object('erro', 'Folha de pagamento inválida.');
  END IF;

  v_fator_r := p_folha_pagamento_12m / p_receita_bruta_12m;
  v_anexo_recomendado := CASE WHEN v_fator_r >= 0.28 THEN 'III' ELSE 'V' END;

  RETURN jsonb_build_object(
    'folha_pagamento_12m', p_folha_pagamento_12m,
    'receita_bruta_12m', p_receita_bruta_12m,
    'fator_r', round(v_fator_r, 6),
    'fator_r_pct', round(v_fator_r * 100, 4),
    'anexo_recomendado', v_anexo_recomendado,
    'regra_aplicada', 'Fator R >= 28% -> Anexo III; Fator R < 28% -> Anexo V (LC 123/2006, art. 18, §5º-J)'
  );
END;
$$;

-- [BLOCO 4] SHA-256 integridade de logs
CREATE OR REPLACE FUNCTION fn_gerar_hash_memoria_calculo()
RETURNS TRIGGER LANGUAGE plpgsql SET search_path = public AS $$
DECLARE
  v_texto_base TEXT;
BEGIN
  v_texto_base := concat_ws('|',
    new.empresa_id::text,
    new.anexo_utilizado,
    new.faixa_utilizada::text,
    new.receita_bruta_12m::text,
    new.aliquota_nominal::text,
    new.valor_deduzir::text,
    new.aliquota_efetiva::text,
    new.valor_nota::text,
    new.valor_devido::text,
    coalesce(new.fator_r::text, ''),
    new.data_calculo::text
  );
  new.hash_integridade := encode(digest(v_texto_base, 'sha256'), 'hex');
  RETURN new;
END;
$$;

-- [BLOCO 2] Simples Nacional Core Engine com Mediana de Validação
CREATE OR REPLACE FUNCTION fn_calcular_simples_nacional(
  p_anexo TEXT,
  p_receita_bruta_12m NUMERIC,
  p_valor_nota NUMERIC,
  p_empresa_id UUID DEFAULT null,
  p_folha_pagamento_12m NUMERIC DEFAULT null,
  p_usuario_id UUID DEFAULT null,
  p_gravar_memoria BOOLEAN DEFAULT true,
  p_cfop TEXT DEFAULT null
) RETURNS JSONB LANGUAGE plpgsql AS $$
DECLARE
  v_faixa RECORD;
  v_aliquota_efetiva NUMERIC(8,6);
  v_valor_simples NUMERIC(15,2);
  v_anexo_original TEXT := p_anexo;
  v_anexo_final TEXT := p_anexo;
  v_fator_r_resultado JSONB;
  v_fator_r_valor NUMERIC(8,6);
  v_anexo_alterado BOOLEAN := false;
  v_e_devolucao BOOLEAN := false;
  v_cfop_tributavel BOOLEAN := true;
  v_cfop_desconhecido BOOLEAN := false;
  v_status_revisao TEXT := 'aprovado';
  v_motivo_revisao TEXT;
BEGIN
  -- Validações básicas de entrada
  IF p_anexo NOT IN ('I', 'II', 'III', 'IV', 'V') THEN
    RETURN jsonb_build_object('erro', format('Anexo "%s" inválido.', p_anexo));
  END IF;
  IF p_valor_nota IS NULL OR p_valor_nota = 0 THEN
    RETURN jsonb_build_object('erro', 'Valor da nota não pode ser zero.');
  END IF;
  IF p_receita_bruta_12m IS NULL OR p_receita_bruta_12m <= 0 THEN
    RETURN jsonb_build_object('erro', 'Receita bruta acumulada inválida.');
  END IF;

  IF p_valor_nota < 0 THEN
    v_e_devolucao := true;
  END IF;

  -- Validação de CFOP
  IF p_cfop IS NOT NULL THEN
    IF NOT EXISTS (SELECT 1 FROM configuracao_cfop_tributavel WHERE cfop = p_cfop) THEN
      v_cfop_desconhecido := true;
      v_status_revisao := 'pendente_revisao';
      v_motivo_revisao := format('CFOP %s não cadastrado no motor.', p_cfop);
    ELSE
      SELECT tributa_simples INTO v_cfop_tributavel FROM configuracao_cfop_tributavel WHERE cfop = p_cfop;
    END IF;
  END IF;

  IF NOT v_cfop_tributavel THEN
    RETURN jsonb_build_object(
      'tributavel', false,
      'cfop', p_cfop,
      'motivo', 'CFOP isento/devolução sem nova tributação simples.'
    );
  END IF;

  -- Fator R
  IF p_folha_pagamento_12m IS NOT NULL AND p_anexo IN ('III', 'V') THEN
    v_fator_r_resultado := fn_calcular_fator_r(p_folha_pagamento_12m, p_receita_bruta_12m);
    IF v_fator_r_resultado ? 'erro' THEN
      RETURN v_fator_r_resultado;
    END IF;
    v_anexo_final := v_fator_r_resultado ->> 'anexo_recomendado';
    v_fator_r_valor := (v_fator_r_resultado ->> 'fator_r')::numeric;
    v_anexo_alterado := (v_anexo_final <> v_anexo_original);
  END IF;

  -- Localização da Faixa
  SELECT * INTO v_faixa FROM simples_nacional_faixas 
  WHERE anexo = v_anexo_final AND status = 'vigente' AND receita_bruta_ate >= p_receita_bruta_12m
  ORDER BY faixa ASC LIMIT 1;

  IF v_faixa IS NULL THEN
    -- Acima do teto, pega a última faixa
    SELECT * INTO v_faixa FROM simples_nacional_faixas 
    WHERE anexo = v_anexo_final AND status = 'vigente'
    ORDER BY faixa DESC LIMIT 1;
  END IF;

  -- Fórmula de alíquota efetiva
  v_aliquota_efetiva := ((p_receita_bruta_12m * v_faixa.aliquota_nominal) - v_faixa.valor_deduzir) / p_receita_bruta_12m;
  IF v_aliquota_efetiva < 0 THEN
    v_aliquota_efetiva := 0;
  END IF;

  v_valor_simples := p_valor_nota * v_aliquota_efetiva;

  -- Memória e gravação
  IF p_gravar_memoria AND p_empresa_id IS NOT NULL THEN
    INSERT INTO simples_nacional_memoria_calculo (
      empresa_id, anexo_utilizado, faixa_utilizada, faixa_id, receita_bruta_12m,
      aliquota_nominal, valor_deduzir, aliquota_efetiva, valor_nota, valor_devido,
      fator_r, anexo_original_informado, anexo_foi_alterado_por_fator_r, usuario_id,
      versao_faixa_utilizada, status_revisao, motivo_revisao, cfop_utilizado, cfop_desconhecido
    ) VALUES (
      p_empresa_id, v_anexo_final, v_faixa.faixa, v_faixa.id, p_receita_bruta_12m,
      v_faixa.aliquota_nominal, v_faixa.valor_deduzir, v_aliquota_efetiva, p_valor_nota, v_valor_simples,
      v_fator_r_valor, v_anexo_original, v_anexo_alterado, p_usuario_id,
      v_faixa.versao, v_status_revisao, v_motivo_revisao, p_cfop, v_cfop_desconhecido
    );
  END IF;

  RETURN jsonb_build_object(
    'anexo_original_informado', v_anexo_original,
    'anexo_utilizado', v_anexo_final,
    'anexo_alterado_por_fator_r', v_anexo_alterado,
    'fator_r', v_fator_r_valor,
    'faixa', v_faixa.faixa,
    'receita_bruta_12m', p_receita_bruta_12m,
    'aliquota_nominal', v_faixa.aliquota_nominal,
    'aliquota_nominal_pct', round(v_faixa.aliquota_nominal * 100, 4),
    'valor_deduzir', v_faixa.valor_deduzir,
    'aliquota_efetiva', round(v_aliquota_efetiva, 6),
    'aliquota_efetiva_pct', round(v_aliquota_efetiva * 100, 4),
    'valor_nota', p_valor_nota,
    'valor_simples_devido', round(v_valor_simples, 2),
    'devolucao', v_e_devolucao,
    'status_revisao', v_status_revisao,
    'motivo_revisao', v_motivo_revisao,
    'fundamento_legal', v_faixa.fundamento_legal
  );
END;
$$;

-- [BLOCO 6] MEI Engine
CREATE OR REPLACE FUNCTION fn_calcular_mei(
  p_empresa_id UUID,
  p_competencia DATE,
  p_tipo_atividade TEXT,
  p_receita_mes NUMERIC,
  p_data_abertura DATE,
  p_receita_acumulada_ano NUMERIC,
  p_usuario_id UUID DEFAULT null,
  p_gravar_memoria BOOLEAN DEFAULT true
) RETURNS JSONB LANGUAGE plpgsql AS $$
DECLARE
  v_parametros RECORD;
  v_das_devido NUMERIC(10,2);
  v_meses_ativos_no_ano INT;
  v_limite_proporcional NUMERIC(15,2);
  v_percentual_usado NUMERIC(6,4);
  v_situacao TEXT;
  v_ano INT;
  v_alerta TEXT;
BEGIN
  IF p_tipo_atividade NOT IN ('comercio_industria', 'servico', 'comercio_e_servico') THEN
    RETURN jsonb_build_object('erro', 'Tipo de atividade MEI inválida.');
  END IF;
  IF p_receita_mes IS NULL OR p_receita_mes < 0 THEN
    RETURN jsonb_build_object('erro', 'Receita do mês inválida.');
  END IF;

  v_ano := extract(year from p_competencia)::int;
  SELECT * INTO v_parametros FROM mei_parametros_anuais WHERE ano = v_ano AND status = 'vigente';

  IF v_parametros IS NULL THEN
    RETURN jsonb_build_object('erro', format('Parâmetros MEI não cadastrados para o ano %s.', v_ano));
  END IF;

  -- Composição DAS
  v_das_devido := v_parametros.das_inss;
  IF p_tipo_atividade IN ('comercio_industria', 'comercio_e_servico') THEN
    v_das_devido := v_das_devido + v_parametros.das_icms;
  END IF;
  IF p_tipo_atividade IN ('servico', 'comercio_e_servico') THEN
    v_das_devido := v_das_devido + v_parametros.das_iss;
  END IF;

  -- Limite Proporcional
  IF extract(year from p_data_abertura) = v_ano THEN
    v_meses_ativos_no_ano := 12 - extract(month from p_data_abertura)::int + 1;
  ELSE
    v_meses_ativos_no_ano := 12;
  END IF;

  v_limite_proporcional := round((v_parametros.limite_faturamento_anual / 12) * v_meses_ativos_no_ano, 2);
  v_percentual_usado := p_receita_acumulada_ano / v_limite_proporcional;

  IF p_receita_acumulada_ano <= v_limite_proporcional THEN
    v_situacao := 'dentro_limite';
  ELSIF p_receita_acumulada_ano <= v_limite_proporcional * (1 + v_parametros.percentual_tolerancia_excesso) THEN
    v_situacao := 'excesso_ate_20';
    v_alerta := 'DAS complementar sobre o excedente é devido. Planeje a migração para Microempresa.';
  ELSE
    v_situacao := 'excesso_acima_20';
    v_alerta := 'Desenquadramento retroativo obrigatório! Tributação retroativa como ME desde janeiro do ano corrente.';
  END IF;

  IF p_gravar_memoria AND p_empresa_id IS NOT NULL THEN
    INSERT INTO mei_memoria_calculo (
      empresa_id, competencia, tipo_atividade, das_devido, receita_mes, receita_acumulada_ano,
      limite_proporcional, percentual_do_limite_usado, situacao, status_revisao, motivo_revisao, usuario_id
    ) VALUES (
      p_empresa_id, p_competencia, p_tipo_atividade, v_das_devido, p_receita_mes, p_receita_acumulada_ano,
      v_limite_proporcional, v_percentual_usado, v_situacao, 
      CASE WHEN v_situacao = 'excesso_acima_20' THEN 'pendente_revisao'::text ELSE 'aprovado'::text END,
      v_alerta, p_usuario_id
    );
  END IF;

  RETURN jsonb_build_object(
    'competencia', p_competencia,
    'tipo_atividade', p_tipo_atividade,
    'das_devido', v_das_devido,
    'composicao_das', jsonb_build_object(
      'inss', v_parametros.das_inss,
      'icms', CASE WHEN p_tipo_atividade IN ('comercio_industria', 'comercio_e_servico') THEN v_parametros.das_icms ELSE 0 END,
      'iss', CASE WHEN p_tipo_atividade IN ('servico', 'comercio_e_servico') THEN v_parametros.das_iss ELSE 0 END
    ),
    'receita_mes', p_receita_mes,
    'receita_acumulada_ano', p_receita_acumulada_ano,
    'limite_proporcional', v_limite_proporcional,
    'percentual_do_limite_usado', round(v_percentual_usado * 100, 2),
    'situacao', v_situacao,
    'alerta', v_alerta,
    'fundamento_legal', v_parametros.fundamento_legal
  );
END;
$$;

-- [BLOCO 7/12] Lucro Presumido Engine
CREATE OR REPLACE FUNCTION fn_calcular_lucro_presumido(
  p_empresa_id UUID,
  p_competencia DATE,
  p_tipo_atividade TEXT,
  p_receita_bruta_mes NUMERIC,
  p_e_venda_produto BOOLEAN,
  p_uf_origem CHAR(2) DEFAULT null,
  p_uf_destino CHAR(2) DEFAULT null,
  p_aliquota_iss NUMERIC DEFAULT null,
  p_usuario_id UUID DEFAULT null,
  p_gravar_memoria BOOLEAN DEFAULT true
) RETURNS JSONB LANGUAGE plpgsql AS $$
DECLARE
  v_param RECORD;
  v_base_irpj NUMERIC(15,2);
  v_base_csll NUMERIC(15,2);
  v_irpj NUMERIC(15,2);
  v_irpj_adicional NUMERIC(15,2);
  v_csll NUMERIC(15,2);
  v_pis NUMERIC(15,2);
  v_cofins NUMERIC(15,2);
  v_icms NUMERIC(15,2) := 0;
  v_iss NUMERIC(15,2) := 0;
  v_aliquota_icms NUMERIC(5,2) := 0;
  v_total_federal NUMERIC(15,2);
  v_total_geral NUMERIC(15,2);
  v_ano INT;
  v_status_revisao TEXT := 'aprovado';
  v_motivo TEXT;
BEGIN
  IF p_receita_bruta_mes IS NULL OR p_receita_bruta_mes <= 0 THEN
    RETURN jsonb_build_object('erro', 'Receita do mês inválida.');
  END IF;

  v_ano := extract(year from p_competencia)::int;
  SELECT * INTO v_param FROM presumido_parametros 
  WHERE ano = v_ano AND tipo_atividade = p_tipo_atividade AND status = 'vigente';

  IF v_param IS NULL THEN
    RETURN jsonb_build_object('erro', format('Parâmetros de presunção não encontrados para %s (%s).', p_tipo_atividade, v_ano));
  END IF;

  -- IRPJ & CSLL
  v_base_irpj := p_receita_bruta_mes * v_param.presuncao_irpj;
  v_base_csll := p_receita_bruta_mes * v_param.presuncao_csll;

  v_irpj := v_base_irpj * v_param.aliquota_irpj;
  IF v_base_irpj > v_param.limite_mensal_adicional THEN
    v_irpj_adicional := (v_base_irpj - v_param.limite_mensal_adicional) * v_param.aliquota_adicional_irpj;
  ELSE
    v_irpj_adicional := 0;
  END IF;

  v_csll := v_base_csll * v_param.aliquota_csll;

  -- PIS & COFINS cumulativo
  v_pis := p_receita_bruta_mes * v_param.aliquota_pis;
  v_cofins := p_receita_bruta_mes * v_param.aliquota_cofins;

  v_total_federal := v_irpj + v_irpj_adicional + v_csll + v_pis + v_cofins;

  -- ICMS / ISS
  IF p_e_venda_produto THEN
    IF p_uf_origem IS NULL OR p_uf_destino IS NULL THEN
      RETURN jsonb_build_object('erro', 'UF Origem e Destino são obrigatórios para vendas físicas.');
    END IF;
    -- Alíquota interestadual simplificada para simulação
    v_aliquota_icms := CASE WHEN p_uf_origem = p_uf_destino THEN 18.0 ELSE 12.0 END;
    v_icms := p_receita_bruta_mes * (v_aliquota_icms / 100);
  ELSE
    IF p_aliquota_iss IS NULL THEN
      v_status_revisao := 'pendente_revisao';
      v_motivo := 'Alíquota de ISS municipal ausente. Verifique o cadastro local do município.';
    ELSE
      v_iss := p_receita_bruta_mes * (p_aliquota_iss / 100);
    END IF;
  END IF;

  v_total_geral := v_total_federal + v_icms + v_iss;

  IF p_gravar_memoria AND p_empresa_id IS NOT NULL THEN
    INSERT INTO presumido_memoria_calculo (
      empresa_id, competencia, tipo_atividade, receita_bruta_mes, base_presumida_irpj, irpj_valor, irpj_adicional,
      base_presumida_csll, csll_valor, pis_valor, cofins_valor, uf_origem, uf_destino, aliquota_icms_applied,
      icms_valor, municipio_iss, aliquota_iss_applied, iss_valor, total_federal, total_geral, status_revisao, motivo_revisao, usuario_id
    ) VALUES (
      p_empresa_id, p_competencia, p_tipo_atividade, p_receita_bruta_mes, v_base_irpj, v_irpj, v_irpj_adicional,
      v_base_csll, v_csll, v_pis, v_cofins, p_uf_origem, p_uf_destino, v_aliquota_icms,
      v_icms, CASE WHEN NOT p_e_venda_produto THEN 'Local' ELSE NULL END, p_aliquota_iss, v_iss, v_total_federal, v_total_geral,
      v_status_revisao, v_motivo, p_usuario_id
    );
  END IF;

  RETURN jsonb_build_object(
    'competencia', p_competencia,
    'tipo_atividade', p_tipo_atividade,
    'receita_bruta_mes', p_receita_bruta_mes,
    'irpj', jsonb_build_object(
      'base_presumida', v_base_irpj,
      'valor', round(v_irpj, 2),
      'adicional', round(v_irpj_adicional, 2)
    ),
    'csll', jsonb_build_object(
      'base_presumida', v_base_csll,
      'valor', round(v_csll, 2)
    ),
    'pis', round(v_pis, 2),
    'cofins', round(v_cofins, 2),
    'icms', CASE WHEN p_e_venda_produto THEN jsonb_build_object('aliquota_pct', v_aliquota_icms, 'valor', round(v_icms, 2)) ELSE null END,
    'iss', CASE WHEN NOT p_e_venda_produto THEN jsonb_build_object('aliquota_pct', p_aliquota_iss, 'valor', round(v_iss, 2)) ELSE null END,
    'total_federal', round(v_total_federal, 2),
    'total_geral_devido', round(v_total_geral, 2),
    'status_revisao', v_status_revisao,
    'motivo_revisao', v_motivo,
    'fundamento_legal', v_param.fundamento_legal
  );
END;
$$;

-- [BLOCO 8] Lucro Real Engine (Compensação Prejuízos 30% e Créditos não-cumulativos)
CREATE OR REPLACE FUNCTION fn_calcular_lucro_real(
  p_empresa_id UUID,
  p_periodo_inicio DATE,
  p_periodo_fim DATE,
  p_lucro_contabil_informado NUMERIC,
  p_receita_bruta_periodo NUMERIC,
  p_adicoes_irpj NUMERIC DEFAULT 0, p_exclusoes_irpj NUMERIC DEFAULT 0,
  p_adicoes_csll NUMERIC DEFAULT 0, p_exclusoes_csll NUMERIC DEFAULT 0,
  p_prejuizo_fiscal_acumulado_anterior NUMERIC DEFAULT 0,
  p_debito_pis NUMERIC DEFAULT null,
  p_credito_pis NUMERIC DEFAULT 0,
  p_debito_cofins NUMERIC DEFAULT null,
  p_credito_cofins NUMERIC DEFAULT 0,
  p_informado_por UUID DEFAULT null,
  p_usuario_id UUID DEFAULT null,
  p_gravar_memoria BOOLEAN DEFAULT true
) RETURNS JSONB LANGUAGE plpgsql AS $$
DECLARE
  v_param RECORD;
  v_meses_periodo INT;
  v_lucro_ajustado_irpj NUMERIC(15,2);
  v_lucro_ajustado_csll NUMERIC(15,2);
  v_prejuizo_compensavel NUMERIC(15,2);
  v_prejuizo_compensado NUMERIC(15,2);
  v_prejuizo_remanescente NUMERIC(15,2);
  v_base_irpj NUMERIC(15,2);
  v_irpj NUMERIC(15,2);
  v_irpj_adicional NUMERIC(15,2);
  v_base_csll NUMERIC(15,2);
  v_csll NUMERIC(15,2);
  v_limite_adicional_periodo NUMERIC(15,2);
  v_final_debito_pis NUMERIC(15,2);
  v_final_debito_cofins NUMERIC(15,2);
  v_pis_a_pagar NUMERIC(15,2);
  v_cofins_a_pagar NUMERIC(15,2);
  v_total_geral NUMERIC(15,2);
  v_ano INT;
  v_status_revisao TEXT := 'aprovado';
  v_motivo TEXT;
BEGIN
  IF p_lucro_contabil_informado IS NULL THEN
    RETURN jsonb_build_object('erro', 'Lucro contábil é obrigatório para Lucro Real.');
  END IF;
  IF p_receita_bruta_periodo IS NULL OR p_receita_bruta_periodo < 0 THEN
    RETURN jsonb_build_object('erro', 'Receita do período inválida.');
  END IF;

  v_ano := extract(year from p_periodo_inicio)::int;
  SELECT * INTO v_param FROM lucro_real_parametros WHERE ano = v_ano AND status = 'vigente';

  IF v_param IS NULL THEN
    RETURN jsonb_build_object('erro', format('Parâmetros de Lucro Real não cadastrados para o ano %s.', v_ano));
  END IF;

  -- Número de meses no faturamento (trimestral vs mensal vs anual)
  v_meses_periodo := greatest(1, (extract(year from p_periodo_fim) - extract(year from p_periodo_inicio)) * 12 + 
                                  extract(month from p_periodo_fim) - extract(month from p_periodo_inicio) + 1);

  -- Adições e exclusões (LALUR)
  v_lucro_ajustado_irpj := p_lucro_contabil_informado + p_adicoes_irpj - p_exclusoes_irpj;
  v_lucro_ajustado_csll := p_lucro_contabil_informado + p_adicoes_csll - p_exclusoes_csll;

  -- Compensação de Prejuízos Fiscais (trava dos 30% pela Lei 9.065/95)
  IF v_lucro_ajustado_irpj > 0 AND p_prejuizo_fiscal_acumulado_anterior > 0 THEN
    v_prejuizo_compensavel := v_lucro_ajustado_irpj * v_param.percentual_max_compensacao_prejuizo;
    v_prejuizo_compensado := least(p_prejuizo_fiscal_acumulado_anterior, v_prejuizo_compensavel);
  ELSE
    v_prejuizo_compensado := 0;
  END IF;

  v_prejuizo_remanescente := greatest(0, p_prejuizo_fiscal_acumulado_anterior - v_prejuizo_compensado);

  v_base_irpj := greatest(0, v_lucro_ajustado_irpj - v_prejuizo_compensado);
  v_base_csll := greatest(0, v_lucro_ajustado_csll - v_prejuizo_compensado);

  -- Cálculos IRPJ e Adicional
  v_irpj := v_base_irpj * v_param.aliquota_irpj;
  v_limite_adicional_periodo := v_param.limite_mensal_adicional * v_meses_periodo;
  IF v_base_irpj > v_limite_adicional_periodo THEN
    v_irpj_adicional := (v_base_irpj - v_limite_adicional_periodo) * v_param.aliquota_adicional_irpj;
  ELSE
    v_irpj_adicional := 0;
  END IF;

  v_csll := v_base_csll * v_param.aliquota_csll;

  -- Débitos PIS/COFINS Não-Cumulativos
  v_final_debito_pis := coalesce(p_debito_pis, p_receita_bruta_periodo * v_param.aliquota_pis_nao_cumulativo);
  v_final_debito_cofins := coalesce(p_debito_cofins, p_receita_bruta_periodo * v_param.aliquota_cofins_nao_cumulativo);

  v_pis_a_pagar := greatest(0, v_final_debito_pis - p_credito_pis);
  v_cofins_a_pagar := greatest(0, v_final_debito_cofins - p_credito_cofins);

  v_total_geral := v_irpj + v_irpj_adicional + v_csll + v_pis_a_pagar + v_cofins_a_pagar;

  -- Sinalização de revisão se créditos forem zerados (muito incomum no Lucro Real)
  IF p_credito_pis = 0 AND p_credito_cofins = 0 THEN
    v_status_revisao := 'pendente_revisao';
    v_motivo := 'Nenhum crédito de PIS/COFINS não-cumulativo foi informado para compensação do período.';
  END IF;

  IF p_gravar_memoria AND p_empresa_id IS NOT NULL THEN
    INSERT INTO lucro_real_memoria_calculo (
      empresa_id, periodo_inicio, periodo_fim, lucro_contabil_informado, informado_por,
      adicoes_irpj, exclusoes_irpj, adicoes_csll, exclusoes_csll, lucro_real_ajustado_irpj,
      lucro_real_ajustado_csll, prejuizo_fiscal_acumulado_anterior, prejuizo_compensado, prejuizo_fiscal_remanescente,
      base_irpj, irpj_valor, irpj_adicional, base_csll, csll_valor, receita_bruta_periodo,
      debito_pis, credito_pis, pis_a_pagar, debito_cofins, credito_cofins, cofins_a_pagar,
      total_geral_devido, status_revisao, motivo_revisao, usuario_id
    ) VALUES (
      p_empresa_id, p_periodo_inicio, p_periodo_fim, p_lucro_contabil_informado, p_informado_por,
      p_adicoes_irpj, p_exclusoes_irpj, p_adicoes_csll, p_exclusoes_csll, v_lucro_ajustado_irpj,
      v_lucro_ajustado_csll, p_prejuizo_fiscal_acumulado_anterior, v_prejuizo_compensado, v_prejuizo_remanescente,
      v_base_irpj, v_irpj, v_irpj_adicional, v_base_csll, v_csll, p_receita_bruta_periodo,
      v_final_debito_pis, p_credito_pis, v_pis_a_pagar, v_final_debito_cofins, p_credito_cofins, v_cofins_a_pagar,
      v_total_geral, v_status_revisao, v_motivo, p_usuario_id
    );
  END IF;

  RETURN jsonb_build_object(
    'periodo', jsonb_build_object('inicio', p_periodo_inicio, 'fim', p_periodo_fim, 'meses', v_meses_periodo),
    'lucro_contabil_informado', p_lucro_contabil_informado,
    'lucro_ajustado_irpj', v_lucro_ajustado_irpj,
    'prejuizo_fiscal', jsonb_build_object(
      'acumulado_anterior', p_prejuizo_fiscal_acumulado_anterior,
      'compensado_neste_periodo', round(v_prejuizo_compensado, 2),
      'remanescente', round(v_prejuizo_remanescente, 2),
      'trava_30pct_aplicada', v_param.percentual_max_compensacao_prejuizo
    ),
    'irpj', jsonb_build_object('base', round(v_base_irpj, 2), 'valor', round(v_irpj, 2), 'adicional', round(v_irpj_adicional, 2)),
    'csll', jsonb_build_object('base', round(v_base_csll, 2), 'valor', round(v_csll, 2)),
    'pis', jsonb_build_object('debito', round(v_final_debito_pis, 2), 'credito', p_credito_pis, 'a_pagar', round(v_pis_a_pagar, 2)),
    'cofins', jsonb_build_object('debito', round(v_final_debito_cofins, 2), 'credito', p_credito_cofins, 'a_pagar', round(v_cofins_a_pagar, 2)),
    'total_geral_devido', round(v_total_geral, 2),
    'status_revisao', v_status_revisao,
    'motivo_revisao', v_motivo,
    'fundamento_legal', v_param.fundamento_legal
  );
END;
$$;

-- [BLOCO 9/28/35] DIFAL Engine com Opção de Base Dupla e Proteção Negativa
CREATE OR REPLACE FUNCTION fn_calcular_difal(
  p_empresa_id UUID,
  p_uf_origem CHAR(2),
  p_uf_destino CHAR(2),
  p_valor_operacao NUMERIC,
  p_e_consumidor_final_nao_contribuinte BOOLEAN,
  p_documento_id UUID DEFAULT null,
  p_usuario_id UUID DEFAULT null,
  p_gravar_memoria BOOLEAN DEFAULT true,
  p_usar_base_dupla BOOLEAN DEFAULT false
) RETURNS JSONB LANGUAGE plpgsql AS $$
DECLARE
  v_aliquota_interna NUMERIC(5,2);
  v_aliquota_interestadual NUMERIC(5,2);
  v_base_difal NUMERIC(15,2);
  v_icms_interestadual NUMERIC(15,2);
  v_difal NUMERIC(15,2);
  v_fcp RECORD;
  v_fcp_valor NUMERIC(15,2) := 0;
  v_status TEXT := 'aprovado';
  v_motivo TEXT;
BEGIN
  IF p_uf_origem = p_uf_destino THEN
    RETURN jsonb_build_object('erro', 'DIFAL não se aplica a operações internas.');
  END IF;
  IF NOT p_e_consumidor_final_nao_contribuinte THEN
    RETURN jsonb_build_object(
      'aplica_difal', false,
      'motivo', 'DIFAL (EC 87/2015) aplica-se somente a vendas interestaduais para não-contribuintes.'
    );
  END IF;
  IF p_valor_operacao IS NULL OR p_valor_operacao <= 0 THEN
    RETURN jsonb_build_object('erro', 'Valor de operação inválido.');
  END IF;

  -- Busca alíquotas oficiais vigentes
  SELECT aliquota_interna INTO v_aliquota_interna FROM tabela_icms_uf WHERE uf = p_uf_destino AND status = 'vigente' LIMIT 1;
  
  -- Se houver interestadual direta
  v_aliquota_interestadual := CASE 
    WHEN p_uf_origem IN ('SP', 'RJ', 'MG', 'PR', 'RS', 'SC') AND p_uf_destino NOT IN ('SP', 'RJ', 'MG', 'PR', 'RS', 'SC') THEN 7.0
    ELSE 12.0
  END;

  IF v_aliquota_interna IS NULL THEN
    RETURN jsonb_build_object('erro', format('UF destino %s não possui alíquota cadastrada.', p_uf_destino));
  END IF;

  -- Proteção contra DIFAL negativo
  IF v_aliquota_interna <= v_aliquota_interestadual THEN
    RETURN jsonb_build_object(
      'aplica_difal', true,
      'difal_valor', 0,
      'total_recolher', 0,
      'status_revisao', 'pendente_revisao',
      'motivo_revisao', format('Alíquota interna destino (%s%%) menor ou igual à interestadual (%s%%). Cenário de DIFAL zerado por segurança.', v_aliquota_interna, v_aliquota_interestadual)
    );
  END IF;

  -- Base Dupla ou Base Simples
  IF p_usar_base_dupla THEN
    v_icms_interestadual := round(p_valor_operacao * (v_aliquota_interestadual / 100), 2);
    v_base_difal := round((p_valor_operacao - v_icms_interestadual) / (1 - (v_aliquota_interna / 100)), 2);
    v_difal := round((v_base_difal * (v_aliquota_interna / 100)) - v_icms_interestadual, 2);
    v_status := 'pendente_revisao';
    v_motivo := 'Cálculo realizado em base dupla. Confirme conformidade legal da UF de destino.';
  ELSE
    v_base_difal := p_valor_operacao;
    v_difal := round(v_base_difal * ((v_aliquota_interna - v_aliquota_interestadual) / 100), 2);
  END IF;

  -- FCP (Fundo Estadual Combate à Pobreza)
  SELECT * INTO v_fcp FROM tabela_fcp_uf WHERE uf = p_uf_destino;
  IF v_fcp IS NOT NULL AND v_fcp.possui_fcp THEN
    v_fcp_valor := round(v_base_difal * (v_fcp.aliquota_fcp_geral / 100), 2);
  END IF;

  IF p_gravar_memoria AND p_empresa_id IS NOT NULL THEN
    INSERT INTO difal_memoria_calculo (
      empresa_id, documento_id, uf_origem, uf_destino, valor_operacao, aliquota_interna_destino,
      aliquota_interestadual, base_difal, difal_valor, fcp_aliquota, fcp_valor, total_a_recolher_destino,
      status_revisao, motivo_revisao, usuario_id
    ) VALUES (
      p_empresa_id, p_documento_id, p_uf_origem, p_uf_destino, p_valor_operacao, v_aliquota_interna,
      v_aliquota_interestadual, v_base_difal, v_difal, coalesce(v_fcp.aliquota_fcp_geral, 0), v_fcp_valor, v_difal + v_fcp_valor,
      v_status, v_motivo, p_usuario_id
    );
  END IF;

  RETURN jsonb_build_object(
    'aplica_difal', true,
    'uf_origem', p_uf_origem,
    'uf_destino', p_uf_destino,
    'valor_operacao', p_valor_operacao,
    'usou_base_dupla', p_usar_base_dupla,
    'base_difal', v_base_difal,
    'aliquota_interna_destino_pct', v_aliquota_interna,
    'aliquota_interestadual_pct', v_aliquota_interestadual,
    'difal_valor', round(v_difal, 2),
    'fcp_valor', round(v_fcp_valor, 2),
    'total_recolher', round(v_difal + v_fcp_valor, 2),
    'status_revisao', v_status,
    'motivo_revisao', v_motivo,
    'fundamento_legal', 'EC 87/2015; LC 190/2022 (DIFAL Não Contribuinte)'
  );
END;
$$;

