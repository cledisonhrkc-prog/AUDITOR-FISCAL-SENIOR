-- [BLOCO 18/19/20] Leitura de Tags XML para NF-e (55), NFC-e (65), CT-e (57) e MDF-e (58)
CREATE OR REPLACE FUNCTION fn_identificar_modelo_documento_xml(p_xml_content TEXT)
RETURNS JSONB LANGUAGE plpgsql AS $$
DECLARE
  v_doc XML;
  v_ns TEXT[][] := ARRAY[ARRAY['nfe', 'http://www.portalfiscal.inf.br/nfe']];
  v_modelo TEXT;
  v_tipo_documento TEXT;
BEGIN
  BEGIN
    v_doc := xmlparse(document p_xml_content);
  EXCEPTION WHEN OTHERS THEN
    RETURN jsonb_build_object('erro', 'XML inválido ou malformado.');
  END;

  v_modelo := (xpath('//nfe:ide/nfe:mod/text()', v_doc, v_ns))[1]::text;
  v_tipo_documento := CASE 
    WHEN v_modelo = '55' THEN 'NF-e'
    WHEN v_modelo = '65' THEN 'NFC-e'
    WHEN v_modelo = '57' THEN 'CT-e'
    WHEN v_modelo = '58' THEN 'MDF-e'
    ELSE 'Desconhecido'
  END;

  RETURN jsonb_build_object(
    'modelo', v_modelo,
    'tipo_documento', v_tipo_documento,
    'reconhecido', v_modelo IN ('55', '65', '57', '58')
  );
END;
$$;

