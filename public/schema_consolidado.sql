CREATE OR REPLACE FUNCTION fn_auditoria_alteracoes()
RETURNS TRIGGER LANGUAGE plpgsql SECURITY DEFINER
SET search_path = public AS $$
DECLARE
  v_usuario_id UUID;
  v_tenant_id UUID;
  v_registro_id TEXT;
  v_dados_antes JSONB;
  v_dados_depois JSONB;
BEGIN
  BEGIN
    v_usuario_id := auth.uid();
  EXCEPTION WHEN OTHERS THEN
    v_usuario_id := NULL;
  END;

  IF TG_OP = 'INSERT' THEN
    v_tenant_id := new.tenant_id;
    v_registro_id := new.id::text;
    v_dados_antes := NULL;
    v_dados_depois := to_jsonb(new);
  ELSIF TG_OP = 'UPDATE' THEN
    v_tenant_id := new.tenant_id;
    v_registro_id := new.id::text;
    v_dados_antes := to_jsonb(old);
    v_dados_depois := to_jsonb(new);
  ELSIF TG_OP = 'DELETE' THEN
    v_tenant_id := old.tenant_id;
    v_registro_id := old.id::text;
    v_dados_antes := to_jsonb(old);
    v_dados_depois := NULL;
  END IF;

  INSERT INTO auditoria_alteracoes (tenant_id, usuario_id, tabela_afetada, registro_id, dados_antes, dados_depois, operacao)
  VALUES (v_tenant_id, v_usuario_id, TG_TABLE_NAME, v_registro_id, v_dados_antes, v_dados_depois, TG_OP);

  IF TG_OP = 'DELETE' THEN
    RETURN old;
  ELSE
    RETURN new;
  END IF;
END;
$$;

